     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        $request->validate([
            'first_name' => 'required',
            'last_name' => 'required',
            'email' => 'required',
            'telephone' => 'required',
        ]);

        Contact::create($request->all());

        return redirect()->route('index')
            ->with('success', 'Contact created successfully.');
    }

