     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        //
        $contacts = Contact::get()->toQuery();
        if (request()->has('search')) {
            $contacts =  $contacts->where('first_name', 'Like', '%' . request()->input('search') . '%');
        }
        $contacts =  $contacts->paginate(2)->withQueryString();
        return view('contact_view', compact('contacts'))->render();
    }

